package bmw;

class Bmw {
	
	String model;
	String color;
	String engineType;
	
	
	Bmw() {
		model = "series3";
		color = "white";
		engineType = "petrol";
	}
	
	Bmw(String model, String color, String engineType) {
		this.model = model;
		this.color = color;
		this.engineType = engineType;
	}	

}



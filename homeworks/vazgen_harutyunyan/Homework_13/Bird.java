

public class Bird{
	private String type;
	private int countOfLegs;

	public Bird(){

	}

	public void setType(String type){
		this.type=type;
	}


	public String getType(){
		return type;
	}


	public void setLegs(int countOfLegs){
		this.countOfLegs=countOfLegs;
	}


	public int getLegs(){
		return countOfLegs;
	}

}